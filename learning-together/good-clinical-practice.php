                </div>
              </div>
              <p>Understanding the safety and efficacy of protocols associated with therapeutic compounds currently in development is of great importance. Clinical guidelines provide the standards for the clinical trials and the current effort is toward an international harmonization of guidelines. It is important for the general pediatricians to be informed about ethical and scientific quality standards used for designing, conducting, recording and reporting trials that involve participation of human subjects to ensure that the rights, safety and wellbeing of the trial subjects are protected.</p>

              <p>Since the 7th Europaediatrics, the biannual European Meeting of EPA-UNEPSA, our Association, has established a fruitful collaboration with the major European Pediatric Specialty Societies, with the aim to disseminate proper scientific and educational information leading to best practice in paediatrics . A constant monitoring of the latest guidelines in Good Clinical Practice in paediatrics issued by the collaborating specialty organizations is performed by EPA-UNEPSA and the link to important guidelines and statements documents is made available on this website in order to provide the European Paediatric Community with relevant information, useful to further promote a good clinical practice in paediatrics.</p>

