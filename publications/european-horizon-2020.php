              <p><b>European Union’s HORIZON 2020 4 years GRANT</b></p>

              <p>“Safe Food for Infants” awarded to EPA-UNEPSA</p>
              <p>EPA-UNEPSA, the Union of National European Pediatric Societies and Associations is pleased to inform you of its recent success and to forward you the related article published in the Journal of Pediatrics.</p>
              <p>We are pleased to inform you that EPA-UNEPSA will represent the European pediatricians affiliated to its members Societies, in the Consortium of scientific centers that was recently awarded a €4 million grant dedicated to the important issue of infant food safety. In particular, during the 4 years of the project, EPA-UNEPSA will closely collaborate as a work package leader, with the French National Research Institute for Agriculture, Food and Environment, which will coordinate the research activities of the program.</p>
              <p>This European Union funded Consortium, has also established a close collaboration with a network of Chinese research centers, which will participate to the common efforts of developing innovative solutions to ensure an effective monitoring of the quality of infant food commercialized in Europe and China.</p>
              <p>During the next four years, EPA-UNEPSA will work in close collaboration with its member Societies to contribute to the overall goal of developing an integrated approach to enhance the identification, assessment, detection and mitigation of safety risks raised by microbiological and chemical hazards all along the EU and Chinese infant food chains. Particularly, the project will benchmark the main safety risks through an extensive hazard identification system based on multiple data sources and a risk ranking procedure. It will also develop procedures to enhance top-down and bottom-up hazard control by combining management options with a panel of technologies for the detection and mitigation of priority hazards.</p>
              <p>The European Union has great expectations on this project. In fact, although it will focus on infant food (i.e. food for children of less than three years) its cross-cutting nature will allow project outcomes to be transferred to most of the other food sectors!!</p>
              <p>EPA-UNEPSA is also in charge of the communication and dissemination of the important achievements that the Consortium will reach during the next four years. This task also cared for by EPA-UNEPSA, will ensure that pediatricians and consumers will be effectively updated about solutions that may significantly contribute to the protection of the European children’s health and wellbeing.</p>
              <p>EPA-UNEPSA is proud to represent the European pediatricians in this important project promoted and funded by the European Union. The paper attached includes an overview of the project and provides you with more detailed information.</p>
