<p><b>Mission</b></p>

To provide a common platform for all general paediatricians in Europe, that will offer them the necessary tools in order to be up-to-date and feel empowered to achieve their personal professional endeavours.

<p><b>Objectives</b></p>

<p>a) To encourage scientific co-operation between not-for-profit National Paediatric Societies/Associations in Europe and between European paediatricians working in primary, secondary and tertiary paediatric care in Europe, in order to promote child health and comprehensive paediatric care.</p>

<p>b) To promote education of patients, families and care givers by translating special knowledge to generalists.</p>

<p>c) To promote research in child health care services in Europe by charitable measures, by no-profit making projects and by activities of benefit to the public.</p>

<p>d) To improve the quality of paediatric patient care in all European countries including both member and non-member states of the European Union by adequate clinical research and by implementing research into practice. EPA pursues a Pan-European approach to reduce diversity of child health care and to improve quality of care.</p>

<p>e) To promote the exchange of national experiences in the various fields of patient care and make national practices and science known to others. To obtain information about paediatrics from national societies and associations, and to make it available to EPA-UNEPSA members.</p>

<p>f) To co-operate scientifically with other not-for-profit paediatric associations worldwide, the World Health Organisation (WHO), UNICEF, the World Bank, and any other national and international organizations, foundations or other statutory corporations and institutions operating in the field of public health care.</p>

<p>g) To organize and arrange congresses for its members and meetings of the President of European National Paediatric Societies/Associations to improve paediatric education.</p>

<p>h) To implement any policy that promotes paediatric health care that may be laid down by the General Assembly, e.g. forming ad hoc expert committees to study and make recommendations on important issues of paediatric interest in Europe.</p>

<p>i) To represent European paediatricians in relation to the International Paediatric Association (IPA), in continuity with the representative function previously performed by UNEPSA.</p>
             
