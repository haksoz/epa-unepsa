                <p><b>A brief overview</b></p>

                <p>The <b>Union of National European Paediatric Societies and Associations</b> (UNEPSA) was founded in 1976 in Rotterdam by 18 European countries. Thirty years later, an article (European Journal of Pediatrics, 2007, vol. 166, no4, pp. 349-357) was co-authored by former presidents and secretaries general, in order to present a retrospective on the activities and achievements of UNEPSA. For an overview of the article with the detailed UNEPSA history, please <a href="/doc/History_fulltext.pdf" target="_blank">click here</a>.</p>

                <p>In the first 30 years of its existence UNEPSA created a forum for the mutual discussion of matters concerning paediatrics. Early on it became clear that the diversity of paediatric care in different countries in Europe is extreme. During the "cold war", UNEPSA was able to cross boundaries between socialist and capitalist countries in Europe, and it was due to the activity of individual members of the UNEPSA council that clinical co-operation and research activities were initiated crossing many political borders.</p>

                <p>In 2007, UNEPSA was renamed <b>European Paediatric Association</b> (EPA/UNEPSA) and a revised constitution was introduced and approved by the General Assembly. EPA/UNEPSA continues the work of UNEPSA and maintains its character and activities.</p>

                <p>Annual meetings of national paediatric presidents focus on the most urgent problems of paediatric health care. "Europaediatrics" is now a biennial congress for all general paediatricians and paediatric sub-specialists in Europe. The main research activities of EPA/UNEPSA concentrate on identifying the demography of primary, secondary and tertiary care paediatrics in Europe. EPA/UNEPSA constantly plans new activities in an effort to demonstrate that it is an active paediatric association counting 38 member countries and representing today more than three quarters of all European countries.</p>
                <p>After more than 30 years, EPA/UNEPSA is still an expanding and vital instrument in improving the medical care of all children and the co-operation of their carers in Europe.</p>


